/**
 * This package provides a JavaFX-based graphical user-interface for browsing
 * the contents of the  <a href="http://librivox.org">LibriVox catalog</a>
 * of M4B public-domain audiobooks. In addition to a number of browse options 
 * (e.g., browse by Genre, Author, Reader, Language; list newest offerings or 
 * random offerings, etc.), it also also provides a facility for download and 
 * automatic import of M4B (audiobook) files into iTunes (if iTunes is locally
 * installed). When properly externally packaged for installation (as with
 * <a href="http://jwrapper.com">JWrapper</a> technology), it can be installed 
 * and run in a Windows, Mac, or Linux environment.
 * <br>
 * This package serves as the UI layer of the 
 * <a href="http://commonvox.org/le-tutorial">LibriVox EXPLORER</a> 
 * desktop application, a 3-tier application consisting of UI 
 * (org.commonvox.le_browser), processing/data-access layer 
 * (org.commonvox.le_catalog), and data-store layer 
 * (org.commonvox.indexedcollection).
 */
